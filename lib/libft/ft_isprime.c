#include "libft.h"

int			ft_isprime(int c)
{
	int	i;

	i = 5;
	if (c <= 1)
		return (0);
	else if (c <= 3)
		return (1);
	else if ((c % 2 == 0) || (c % 3 == 0))
		return (0);
	while (i * i <= c)
	{
		if ((c % i == 0) || (c % (i + 2) == 0))
			return (0);
		i = i + 6;
	}
	return (1);
}
